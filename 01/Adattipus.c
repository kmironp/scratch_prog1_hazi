#include<stdio.h>

int main()
{
    int egesz;
    egesz = 23;
    printf("Egesz: %d\n", egesz);
    printf(".........\n");
    
    char kar = 'n';
    printf("Karakter: %c\n", kar);
    printf(".........\n");
    
    float tort = 3.1415;
    printf("Float: %f\n", tort);
    printf(".........\n");
    
    double tort2;
    tort2 = 9.1223;
    printf("Double: %.5lf\n", tort2);
    printf(".........\n");
    
    return 0;
}
